int	str_len(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	c_ws(char *line)
{
	int	i;

	i = 0;
	while ((line[i] == ' ' || line[i] == '\t' || line[i] == '\n')
		|| (line[i] == '\r' || line[i] == '\v' || line[i] == '\f'))
		(i)++;
	return (i);
}
